import fs from 'node:fs'
import { createRequire } from 'node:module'
import wordListPath from 'word-list'

const require = createRequire(import.meta.url)
const dictionaryHe = require('dictionary-he')

const MAX_LENGTH = 8
const MIN_LENGTH = 2

function uniqueSorted(words) {
  return [...new Set(words)].sort((a, b) => a.localeCompare(b))
}

const english = uniqueSorted(
  fs.readFileSync(wordListPath, 'utf8')
    .split(/\r?\n/)
    .map(word => word.trim().toUpperCase())
    .filter(word => word.length >= MIN_LENGTH && word.length <= MAX_LENGTH && /^[A-Z]+$/.test(word))
)

const hebrewDictionary = await new Promise((resolve, reject) => {
  dictionaryHe((error, dictionary) => error ? reject(error) : resolve(dictionary))
})

const hebrew = uniqueSorted(
  hebrewDictionary.dic.toString('utf8')
    .split(/\r?\n/)
    .slice(1)
    .map(line => line.split(/[\/\s]/, 1)[0]?.trim())
    .filter(word => word && word.length >= MIN_LENGTH && word.length <= MAX_LENGTH && /^[א-תךםןףץ]+$/.test(word))
)

if (!english.includes('DEAR')) throw new Error('English dictionary generation failed: DEAR is missing')
if (english.length < 20000) throw new Error(`English dictionary unexpectedly small: ${english.length}`)
if (hebrew.length < 5000) throw new Error(`Hebrew dictionary unexpectedly small: ${hebrew.length}`)

const source = `// Generated by scripts/build-free-dictionary.mjs. Do not hand-edit.\nexport type FreeLanguage = 'en' | 'he'\n\nexport const FREE_WORDS: Record<FreeLanguage, string[]> = ${JSON.stringify({ en: english, he: hebrew })}\n\nexport function createVocabulary(words: string[]) {\n  const normalized = [...new Set(words.map(word => word.trim().toUpperCase()).filter(Boolean))]\n  const wordSet = new Set(normalized)\n  const prefixSet = new Set<string>()\n  for (const word of normalized) for (let index = 1; index <= word.length; index += 1) prefixSet.add(word.slice(0, index))\n  return { words: normalized, isWord: (value: string) => wordSet.has(value.toUpperCase()), isPrefix: (value: string) => prefixSet.has(value.toUpperCase()) }\n}\n\nconst CORE_INFO: Record<string, [string, string]> = {\n  'בית':['house','bayit'],'ספר':['book','sefer'],'מים':['water','mayim'],'אור':['light','or'],'דלת':['door','delet'],'חלון':['window','chalon'],'מיטה':['bed','mita'],'כוס':['cup','kos'],'כיסא':['chair','kise'],'שולחן':['table','shulchan'],'יקר':['dear','yakar']\n}\n\nexport function getFreeWordInfo(word: string, language: FreeLanguage) {\n  const normalized = word.toUpperCase()\n  if (language === 'he') {\n    const [translation, transliteration] = CORE_INFO[word] ?? ['Translation unavailable', 'Transliteration unavailable']\n    return { word, translation, transliteration }\n  }\n  const reverse = Object.entries(CORE_INFO).find(([, [translation]]) => translation.toUpperCase() === normalized)\n  return { word: normalized, translation: reverse?.[0] ?? 'תרגום לא זמין', transliteration: normalized.toLowerCase() }\n}\n`

fs.writeFileSync(new URL('../src/data/freeVocabulary.ts', import.meta.url), source)
console.log(`Generated ${english.length} English and ${hebrew.length} Hebrew Free-mode words.`)
