import fs from 'node:fs'
import wordListPath from 'word-list'
import hebrewDictionary from 'dictionary-he'

const MAX_LENGTH = 8
const MIN_LENGTH = 2

function uniqueSorted(words) {
  return [...new Set(words)].sort((a, b) => a.localeCompare(b))
}

const english = uniqueSorted(
  fs.readFileSync(wordListPath, 'utf8')
    .split(/\r?\n/)
    .map(word => word.trim().toUpperCase())
    .filter(word => word.length >= MIN_LENGTH && word.length <= MAX_LENGTH && /^[A-Z]+$/.test(word))
)

const hebrew = uniqueSorted(
  hebrewDictionary.dic.toString('utf8')
    .split(/\r?\n/)
    .slice(1)
    .map(line => line.split(/[\/\s]/, 1)[0]?.trim())
    .filter(word => word && word.length >= MIN_LENGTH && word.length <= MAX_LENGTH && /^[א-תךםןףץ]+$/.test(word))
)

if (!english.includes('DEAR')) throw new Error('English dictionary generation failed: DEAR is missing')
if (english.length < 20000) throw new Error(`English dictionary unexpectedly small: ${english.length}`)
if (hebrew.length < 5000) throw new Error(`Hebrew dictionary unexpectedly small: ${hebrew.length}`)

function writeWordModule(language, words) {
  const source = `// Generated by scripts/build-free-dictionary.mjs. Do not hand-edit.\nconst words: string[] = ${JSON.stringify(words)}\nexport default words\n`
  fs.writeFileSync(new URL(`../src/data/freeWords.${language}.ts`, import.meta.url), source)
}

writeWordModule('en', english)
writeWordModule('he', hebrew)
console.log(`Generated ${english.length} English and ${hebrew.length} Hebrew Free-mode words as lazy chunks.`)
